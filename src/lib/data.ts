export const getProducts = (): Product[] => products.filter(p => p.type !== 'variable' && p.productType === 'perfume');

export const getProductBySlug = (slug: string): Product | undefined => {
  return getProducts().find(p => p.slug === slug);
};

export const getRelatedProducts = (currentProduct: Product): Product[] => {
  return getProducts().filter(p => 
      p.id !== currentProduct.id && 
      (p.family === currentProduct.family || p.brand.name === currentProduct.brand.name)
  ).slice(0, 4);
};

export const getBrands = (): Brand[] => {
  const productBrands = getProducts().map(p => p.brand).filter(b => b); // filter out undefined brands
  const uniqueBrandIds = new Set<string>();
  return productBrands.filter(brand => {
    if (!uniqueBrandIds.has(brand.id)) {
      uniqueBrandIds.add(brand.id);
      return true;
    }
    return false;
  });
};

